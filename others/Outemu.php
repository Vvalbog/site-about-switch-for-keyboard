<p>Очередные китайские клоны переключателей Cherry MX. Одни из самых недорогих механических переключателей, известных за пределами Китая. Стали популярными благодаря использованию в супер-дешевых китайских клавиатурах Motospeed (можно найти за $30). Хорошего качества за такие деньги ждать не стоит. Основная проблема – неконсистентность. Переключатели одного цвета на одной и той же клавиатуре могут различаться между собой по силе нажатия, звуку и ощущениям в целом (хотя это общая проблема многих китайских переключателей). О высоком качестве пластика и выплавки контактов тоже говорить не приходится.</p>
<p>По цвету различичия такие же, как и у Cherry MX: существуют коричневые, синие, красные и черные переключатели. Тактильные, кликающие и линейные соответственно. В отличие от Cherry, Outemu более тугие.&nbsp;</p>
