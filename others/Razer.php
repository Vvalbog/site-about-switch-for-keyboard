<h2 id="razer"><span>Razer</span><span></span>&nbsp;</h2>
<p><img src="https://static.insales-cdn.com/files/1/2109/3541053/original/razer-switches-list.jpg" alt="Переключатели Razer" width="453" height="231"></p>
<p>Переключатели Razer – это клоны переключателей Cherry MX. В 2014-2015 году рынок механических клавиатур столкнулся с дефицитом переключателей Cherry MX. Производственных мощностей компании Cherry не хватало, чтобы удовлетворить растущий спрос на переключатели. Поэтому производителям пришлось искать другие варианты. В первую очередь именно это, а не желание удешевить производство, было основной причиной перехода многих команий, в том числе Razer, на китайские аналоги Cherry MX.&nbsp;</p>
<p>На момент написания статьи существует как минимум 2 производителя переключателей Razer:&nbsp;<span>Kaihua (производят переключатели Kailh) и Greetech. По слухам, все клавиатуры, начиная с конца 2015 года, оснащаются переключателями производства Greetech. Опять же, по слухам, Razer отказались от переключателей Kailh из-за нестабильного качества. &nbsp;&nbsp;</span></p>
<p><span>Основные нападки на переключатели Razer заключаются в неконсистентности. Ощущения от нажатия от переключателя к переключателю могут быть разными: одни более громко кликают, в других клик еле слышен. У одних ход более плавный, у других – менее. &nbsp;&nbsp;</span></p>
